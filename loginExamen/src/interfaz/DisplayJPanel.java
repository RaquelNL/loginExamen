	public void displayPerson(List<Person> empleados, int incArray) {

		try {

			nameTextField.setText(((Person) empleados.get(incArray)).getName());
			emailTextField.setText(((Person) empleados.get(incArray)).getEmail());
			ssnTextField.setText(((Person) empleados.get(incArray)).getSsn());
			dobTextField.setText(((Person) empleados.get(incArray)).getDob());
			geoTextField.setText(((Person) empleados.get(incArray)).getGeographic_data());
			bankTextField.setText(((Person) empleados.get(incArray)).getBankAccNum());
			phoneTextField.setText(((Person) empleados.get(incArray)).getPhone());
			jLabel12_1.setText(String.valueOf(((Person) empleados.get(incArray)).getEmpcode()));
			lblDepartamento_1.setText(((Person) empleados.get(incArray)).getDepartamento());
			textFieldin.setText(((Person) empleados.get(incArray)).getIncorporacion());
			sueldoTextField.setText(String.valueOf(((Person) empleados.get(incArray)).getSueldo()));

			String imageTxt = ((Person) empleados.get(incArray)).getImagetxt();
			// imagePatTxt.setText(imageTxt);

			try {
				BufferedImage img = ImageIO.read(new File(imageTxt));

				imageLabel.setBounds(100, 100, 280, 250);

				Image dimg = img.getScaledInstance(imageLabel.getWidth(), imageLabel.getHeight(), Image.SCALE_SMOOTH);
				ImageIcon icon = new ImageIcon(dimg);

				imageLabel.setIcon(icon);
			} catch (IOException e) {
				e.printStackTrace();
			}
		} catch (NullPointerException e) {
			System.out.println("No hay ningun perfil creado");
		}

	}

